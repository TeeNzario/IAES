export const CURRICULUMS: Curriculum[] = CURRICULUM_ROWS.map(
  ([facultyName, id, name, legacyIds = []]) => ({
    id,
    code: id,
    name,
    facultyName,
    facultyCode: FACULTY_CODE_BY_NAME[facultyName],
    legacyIds: [...legacyIds],
  }),
);

function normalizeCurriculumText(
  id: string | number | null | undefined,
): string | undefined {
  if (id == null || id === "") return undefined;
  return String(id).trim();
}

export function getCurriculumById(
  id: string | number | null | undefined,
): Curriculum | undefined {
  const normalized = normalizeCurriculumText(id);
  if (!normalized) return undefined;

  const codePart = normalized.split(":")[0]?.trim() ?? normalized;
  return CURRICULUMS.find(
    (curriculum) =>
      curriculum.id === normalized ||
      curriculum.code === normalized ||
      curriculum.name === normalized ||
      curriculum.id === codePart ||
      curriculum.code === codePart ||
      curriculum.legacyIds.includes(normalized) ||
      curriculum.legacyIds.includes(codePart),
  );
}

export function resolveCurriculumId(
  value: string | number | null | undefined,
  fallback = "",
): string {
  const curriculum = getCurriculumById(value);
  if (curriculum) return curriculum.id;
  const normalized = normalizeCurriculumText(value);
  return normalized ?? fallback;
}

export function getCurriculumName(
  id: string | number | null | undefined,
  fallback = "-",
): string {
  const curriculum = getCurriculumById(id);
  return curriculum ? curriculum.name : fallback;
}

export function getCurriculumsByFacultyCode(
  facultyCode: number | null | undefined,
): Curriculum[] {
  if (facultyCode == null) return CURRICULUMS;
  return CURRICULUMS.filter((curriculum) => curriculum.facultyCode === facultyCode);
}
